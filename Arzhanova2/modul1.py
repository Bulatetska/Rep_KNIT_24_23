def add(x, y):
    """Повертає суму двох чисел x і y."""
    return x + y
def subtract(x, y):
    """Повертає різницю двох чисел x і y."""
    return x - y

def multiply(x, y):
    """Повертає добуток двох чисел x і y."""
    return x * y

def divide(x, y):
    """Повертає частку двох чисел x і y."""
    return x / y