def concatenate(str1, str2):
    """Повертає об'єднання двох рядків str1 і str2."""
    return str1 + str2

def capitalize(string):
    """Повертає рядок, у якому перший символ велика літера, а решта символів маленькі."""
    return string.capitalize()

def count_occurrences(string, substring):
    """Повертає кількість входжень підрядка substring у рядок string."""
    return string.count(substring)